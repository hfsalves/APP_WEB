SELECT
    LEFT(NEWID(), 25),
    C.ORDEM,
    C.NMCAMPO,
    C.DESCRICAO,
    C.TIPO,
    'AL',
    0,
    0,
    0,
    0,
    '',
    '',
    C.TAM,
    C.ORDEM_MOBILE,
    C.TAM_MOBILE,
    '',
    0,
    '',
    '',
    CASE WHEN C.TIPO = 'INT' THEN 0 ELSE 2 END,
    -999999999,
    999999999,
    '',
    1,
    0,
    5,
    0,
    5,
    0,
    0,
    1,
    '',
    N'{}'
FROM @campos C
WHERE NOT EXISTS (
    SELECT 1
    FROM dbo.CAMPOS X
    WHERE UPPER(LTRIM(RTRIM(X.TABELA))) = 'AL'
      AND UPPER(LTRIM(RTRIM(X.NMCAMPO))) = C.NMCAMPO
);

UPDATE X
SET
    X.DESCRICAO = C.DESCRICAO,
    X.TIPO = C.TIPO,
    X.VISIVEL = 1,
    X.TAM = C.TAM,
    X.ORDEM_MOBILE = C.ORDEM_MOBILE,
    X.TAM_MOBILE = C.TAM_MOBILE
FROM dbo.CAMPOS X
JOIN @campos C
  ON UPPER(LTRIM(RTRIM(X.NMCAMPO))) = C.NMCAMPO
WHERE UPPER(LTRIM(RTRIM(X.TABELA))) = 'AL';

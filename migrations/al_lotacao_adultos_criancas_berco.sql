INSERT INTO dbo.CAMPOS (
    CAMPOSSTAMP,
    ORDEM,
    NMCAMPO,
    DESCRICAO,
    TIPO,
    TABELA,
    LISTA,
    FILTRO,
    ADMIN,
    RONLY,
    COMBO,
    VIRTUAL,
    TAM,
    ORDEM_MOBILE,
    TAM_MOBILE,
    CONDICAO_VISIVEL,
    OBRIGATORIO,
    obrigatorio_se,
    formula,
    decimais,
    minimo,
    maximo,
    FILTRODEFAULT,
    VISIVEL,
    ORDEM_LISTA,
    TAM_LISTA,
    ORDEM_LISTA_MOBILE,
    TAM_LISTA_MOBILE,
    LISTA_MOBILE_BOLD,
    LISTA_MOBILE_ITALIC,

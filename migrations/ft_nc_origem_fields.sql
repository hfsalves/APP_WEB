    FROM sys.indexes
    WHERE name = 'IX_FI_FISTAMP_ORIGEM'
      AND object_id = OBJECT_ID('dbo.FI')
)
BEGIN
    CREATE INDEX IX_FI_FISTAMP_ORIGEM
    ON dbo.FI (FISTAMP_ORIGEM);
END
GO
